package com.algebratech.pulse_wellness.models;

public class EnableNotificationModel {
    String name;
    boolean isCheck;

    public EnableNotificationModel(String name, boolean isCheck) {
        this.name = name;
        this.isCheck = isCheck;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isCheck() {
        return isCheck;
    }

    public void setCheck(boolean check) {
        isCheck = check;
    }
}
